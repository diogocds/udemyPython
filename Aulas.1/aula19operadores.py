
# Operadores de comparação (relacionais)
# Op    significado    todos os exemplo são (True)
# >       Maior       2 > 1
# >=      Maior ou Igual      2 >= 2
# <      Menor       1 > 2
# <=     Menor ou Igual      2 <= 2
# ==     Igual             ("a" == "a")
# !=      Diferente     "a" != "b
...
maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(diferente, maior, maior_ou_igual, menor, menor_ou_igual, igual)

