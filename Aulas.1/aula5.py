# Tipo de dado boolean
# Ao questionar algo em um programa,
# Só existe duas respostas possíveis
# sim (true) ou nao( false).
# Existem vários operadores pa "questionnar".
# Detre eles o==, que é um operador lógico que
# questiona se um valor é igual a outro
print(10==10) # sim => True (Verdadeiro)
print(10==11)# Não => False (Falso)
print(type(False))
print(type(True))
print(type(10==10))
print(type(10==11))
